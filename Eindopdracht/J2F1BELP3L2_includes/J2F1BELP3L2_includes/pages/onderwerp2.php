<h1>Minecraft</h1>
<p>Minecraft is a popular video game that was first released in 2011. The game is designed to allow players to explore, build and create their own virtual world using various types of blocks. Minecraft offers two main modes of play: survival mode and creative mode.

In survival mode, players must gather resources and build structures in order to survive in a dangerous and ever-changing world. They must fend off enemies such as zombies, spiders, and skeletons while also finding food, shelter, and other necessary resources. Survival mode can be played alone or with friends, either online or locally.</p>
<img src="images/minecraft.png" alt="Minecraft">
<p>Creative mode, on the other hand, offers unlimited resources and allows players to build anything they can imagine without the need to gather materials or fend off enemies. This mode is ideal for those who prefer to focus on designing and creating rather than survival.

Minecraft also offers a multiplayer mode, where players can join forces to build and explore together. The game has a large and dedicated community that creates mods, skins, and other add-ons to enhance the game experience.

Overall, Minecraft is a highly versatile game that offers endless possibilities for creativity, exploration, and adventure. Its unique gameplay and open-ended world make it a beloved game among players of all ages.</p>